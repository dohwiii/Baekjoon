SELECT CAR_ID, 
if(CAR_ID IN (SELECT h.CAR_ID FROM CAR_RENTAL_COMPANY_RENTAL_HISTORY h
              WHERE h.START_DATE <= '2022-10-16' AND h.END_DATE >= '2022-10-16'), '대여중', '대여 가능') AS AVAILABILITY
FROM CAR_RENTAL_COMPANY_RENTAL_HISTORY 
GROUP BY CAR_ID
ORDER BY CAR_ID DESC;

